CREATE DATABASE EMPLOYEE_DETAIL;
USE EMPLOYEE_DETAIL;

CREATE TABLE EMPLOYEE (
Emp_id int(5) PRIMARY KEY AUTO_INCREMENT,
Name varchar(20),
DOB varchar(10),
Gender varchar(10),
Pincode int(10),
Phone int(10) UNIQUE,
Salary int(10),
Job_Id int,
Dept_Id int,
FOREIGN KEY(Pincode) REFERENCES ADDRESS(Pincode),
FOREIGN KEY(Job_Id) REFERENCES JOBS(Job_Id),
FOREIGN KEY(Dept_Id) REFERENCES DEPARTMENT(Dept_Id)
);
ALTER TABLE EMPLOYEE AUTO_INCREMENT = 101;

CREATE TABLE ADDRESS (
Pincode int PRIMARY KEY,
State varchar(20),
District varchar(20),
Town varchar(20),
Area varchar(20)
);

CREATE TABLE JOBS (
Job_Id int PRIMARY KEY AUTO_INCREMENT,
Title varchar(20),
Description varchar(20),
Dept_Id int,
FOREIGN KEY(Dept_Id) REFERENCES DEPARTMENT(Dept_Id)
);
ALTER TABLE JOBS AUTO_INCREMENT = 1001;

CREATE TABLE DEPARTMENT (
Dept_Id int PRIMARY KEY AUTO_INCREMENT,
Code varchar(20),
Description varchar(20)
);
ALTER TABLE DEPARTMENT AUTO_INCREMENT = 10001;

CREATE TABLE VACANCY (
Vacancy_No int PRIMARY KEY AUTO_INCREMENT,
Job_Id int,
Dept_Id int,
No_of_Vacancy int,
Date_of_Opening date,
Date_of_Closing date,
FOREIGN KEY(Dept_Id) REFERENCES DEPARTMENT(Dept_Id),
FOREIGN KEY(Job_Id) REFERENCES JOBS(Job_Id)
);
ALTER TABLE VACANCY AUTO_INCREMENT = 405060;

